import matplotlib.pyplot as plt
import pandas as pd
import numpy as np


# 1225 frame
data_1225 = np.loadtxt(
    "1225.txt",
    delimiter="\t",
    dtype=float
)

print(data_1225.shape)
x = data_1225[:, 0]
y = data_1225[:, 1]
print(x.shape)
print(y.shape)

plt.scatter(x, y)
plt.axis('equal')
plt.xlim([-50, 600])
plt.ylim([0, 350])
ax = plt.gca()
ax.xaxis.set_ticks_position('top')
ax.invert_yaxis()
plt.title("Point Distribution @ 1225 Frame")
plt.show()


